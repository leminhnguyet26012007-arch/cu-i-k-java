    // --- DATABASE INITIALIZATION ---
    public void initializeDatabaseSchema() {
        try (Connection conn = getConnection();
             Statement stmt = conn.createStatement()) {

            // 0. Ensure Tables Exist
            // Users
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS users (" +
                               "id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, " +
                               "username VARCHAR(50) UNIQUE, " +
                               "password VARCHAR(255), " +
                               "email VARCHAR(100), " +
                               "role VARCHAR(20), " +
                               "locked BOOLEAN DEFAULT FALSE)");

            // SinhVien
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS sinh_vien (" +
                               "ma_sv VARCHAR(20) PRIMARY KEY, " +
                               "ho_ten VARCHAR(100), " +
                               "lop VARCHAR(20), " +
                               "email VARCHAR(255), " +
                               "sdt VARCHAR(255), " +
                               "user_id BIGINT, " +
                               "tuition_paid BOOLEAN DEFAULT FALSE, " +
                               "FOREIGN KEY (user_id) REFERENCES users(id))");

            // GiangVien
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS giang_vien (" +
                               "ma_gv VARCHAR(20) PRIMARY KEY, " +
                               "ho_ten VARCHAR(100), " +
                               "email VARCHAR(255), " +
                               "sdt VARCHAR(255), " +
                               "user_id BIGINT, " +
                               "FOREIGN KEY (user_id) REFERENCES users(id))");

            // MonHoc
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS mon_hoc (" +
                               "ma_mh VARCHAR(20) PRIMARY KEY, " +
                               "ten_mh VARCHAR(100), " +
                               "so_tin_chi INT)");

            // LopHocPhan
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS lop_hoc_phan (" +
                               "id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, " +
                               "ma_lhp VARCHAR(20), " +
                               "phong_hoc VARCHAR(20), " +
                               "lich_hoc VARCHAR(50), " +
                               "si_so_toi_da INT DEFAULT 100, " +
                               "si_so_hien_tai INT DEFAULT 0, " +
                               "mon_hoc_ma_mh VARCHAR(20), " +
                               "giang_vien_ma_gv VARCHAR(20), " +
                               "FOREIGN KEY (mon_hoc_ma_mh) REFERENCES mon_hoc(ma_mh), " +
                               "FOREIGN KEY (giang_vien_ma_gv) REFERENCES giang_vien(ma_gv))");

            // DangKyHoc
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS dang_ky_hoc (" +
                               "id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, " +
                               "sinh_vien_ma_sv VARCHAR(20), " +
                               "lop_hoc_phan_id BIGINT, " +
                               "FOREIGN KEY (sinh_vien_ma_sv) REFERENCES sinh_vien(ma_sv), " +
                               "FOREIGN KEY (lop_hoc_phan_id) REFERENCES lop_hoc_phan(id))");

            // Diem
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS diem (" +
                               "id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, " +
                               "sinh_vien_ma_sv VARCHAR(20), " +
                               "lop_hoc_phan_id BIGINT, " +
                               "diemqt DOUBLE PRECISION DEFAULT 0, " +
                               "diem_thi DOUBLE PRECISION DEFAULT 0, " +
                               "diem_tong_ket DOUBLE PRECISION DEFAULT 0, " +
                               "locked BOOLEAN DEFAULT FALSE, " +
                               "FOREIGN KEY (sinh_vien_ma_sv) REFERENCES sinh_vien(ma_sv), " +
                               "FOREIGN KEY (lop_hoc_phan_id) REFERENCES lop_hoc_phan(id))");

            // Attendance
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS attendance (" +
                               "id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, " +
                               "lop_hoc_phan_id BIGINT, " +
                               "sinh_vien_ma_sv VARCHAR(20), " +
                               "ngay_diem_danh DATE, " +
                               "co_mat BOOLEAN DEFAULT FALSE)");

            // PhanHoi
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS phan_hoi (" +
                               "id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, " +
                               "sinh_vien_ma_sv VARCHAR(20), " +
                               "tieu_de VARCHAR(255), " +
                               "noi_dung TEXT, " +
                               "loai_don VARCHAR(50), " +
                               "ngay_gui TIMESTAMP, " +
                               "trang_thai VARCHAR(20))");

            // System Config
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS system_config (" +
                               "id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, " +
                               "current_year VARCHAR(20), " +
                               "current_semester VARCHAR(10), " +
                               "tuition_per_credit DOUBLE PRECISION)");

            // Notifications
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS notifications (" +
                               "id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, " +
                               "title VARCHAR(255), " +
                               "content TEXT, " +
                               "target_role VARCHAR(20), " +
                               "target_id VARCHAR(50), " +
                               "created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP)");

            // Logs table
            stmt.executeUpdate("CREATE TABLE IF NOT EXISTS system_logs (" +
                               "id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, " +
                               "timestamp TIMESTAMP, " +
                               "username VARCHAR(50), " +
                               "action VARCHAR(100), " +
                               "status VARCHAR(20), " +
                               "details TEXT)");

            ensureUsersTableColumns(conn);

            stmt.executeUpdate("INSERT INTO system_config (current_year, current_semester, tuition_per_credit) " +
                               "SELECT '2025-2026', '1', 500000 WHERE NOT EXISTS (SELECT * FROM system_config)");

            System.out.println("[Server Schema] Database schema ready.");

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    /** Thêm cột locked nếu bảng users đã được tạo trước đó bởi JPA. */
    private void ensureUsersTableColumns(Connection conn) {
        try (Statement stmt = conn.createStatement()) {
            stmt.executeUpdate("ALTER TABLE users ADD COLUMN IF NOT EXISTS locked BOOLEAN DEFAULT FALSE");
        } catch (SQLException e) {
            System.err.println("[Server Schema] Không thể thêm cột locked: " + e.getMessage());
        }
    }

    private long findUserId(Connection conn, String username) throws SQLException {
        try (PreparedStatement ps = conn.prepareStatement("SELECT id FROM users WHERE username = ?")) {
            ps.setString(1, username);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    return rs.getLong(1);
                }
            }
        }
        return -1;
    }

