    public List<LopHocPhan> getAllClasses() {
        List<LopHocPhan> list = new ArrayList<>();
        String sql = "SELECT l.*, m.ten_mh, g.ho_ten as gv_ten FROM lop_hoc_phan l " +
                     "LEFT JOIN mon_hoc m ON l.mon_hoc_ma_mh = m.ma_mh " +
                     "LEFT JOIN giang_vien g ON l.giang_vien_ma_gv = g.ma_gv";
        try (Connection conn = getConnection();
             PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                LopHocPhan lhp = new LopHocPhan();
                lhp.setId(rs.getLong("id"));
                lhp.setMaLhp(rs.getString("ma_lhp"));
                lhp.setPhongHoc(rs.getString("phong_hoc"));
                lhp.setLichHoc(rs.getString("lich_hoc"));
                lhp.setSiSoHienTai(rs.getInt("si_so_hien_tai"));
                lhp.setSiSoToiDa(rs.getInt("si_so_toi_da"));

                MonHoc mh = new MonHoc();
                mh.setMaMH(rs.getString("mon_hoc_ma_mh"));
                mh.setTenMH(rs.getString("ten_mh"));
                lhp.setMonHoc(mh);

                if (rs.getString("giang_vien_ma_gv") != null) {
                    GiangVien gv = new GiangVien();
                    gv.setMaGV(rs.getString("giang_vien_ma_gv"));
                    gv.setHoTen(rs.getString("gv_ten"));
                    lhp.setGiangVien(gv);
                } else {
                    GiangVien gvTrong = new GiangVien();
                    gvTrong.setHoTen("Chưa phân công");
                    lhp.setGiangVien(gvTrong);
                }

                list.add(lhp);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return list;
    }

