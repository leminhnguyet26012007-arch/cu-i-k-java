    public List<Diem> getStudentGrades(String maSV) {
        List<Diem> list = new ArrayList<>();
        String sql = "SELECT d.*, sv.ho_ten, sv.ma_sv AS student_ma_sv, m.ten_mh, m.so_tin_chi, l.ma_lhp " +
                     "FROM diem d JOIN sinh_vien sv ON d.sinh_vien_ma_sv = sv.ma_sv " +
                     "JOIN lop_hoc_phan l ON d.lop_hoc_phan_id = l.id " +
                     "JOIN mon_hoc m ON l.mon_hoc_ma_mh = m.ma_mh " +
                     "WHERE d.sinh_vien_ma_sv = ? ORDER BY m.ten_mh";
        try (Connection conn = getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, maSV);
            try (ResultSet rs = ps.executeQuery()) {
                while (rs.next()) {
                    list.add(mapDiemRow(rs));
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return list;
    }

    private Diem mapDiemRow(ResultSet rs) throws SQLException {
        Diem d = new Diem();
        d.setId(rs.getLong("id"));
        d.setDiemQT(rs.getDouble("diemqt"));
        d.setDiemThi(rs.getDouble("diem_thi"));
        d.setDiemTongKet(rs.getDouble("diem_tong_ket"));
        d.setDiemChu(computeLetterGrade(d.getDiemTongKet()));
        try {
            d.setLocked(rs.getBoolean("locked"));
        } catch (SQLException e) {
            d.setLocked(false);
        }
        SinhVien sv = new SinhVien();
        sv.setMaSV(readString(rs, "student_ma_sv", "sinh_vien_ma_sv", "ma_sv"));
        sv.setHoTen(rs.getString("ho_ten"));
        d.setSinhVien(sv);
        LopHocPhan lhp = new LopHocPhan();
        lhp.setMaLhp(rs.getString("ma_lhp"));
        MonHoc mh = new MonHoc();
        mh.setTenMH(rs.getString("ten_mh"));
        mh.setSoTinChi(rs.getInt("so_tin_chi"));
        lhp.setMonHoc(mh);
        d.setLopHocPhan(lhp);
        return d;
    }

    private String readString(ResultSet rs, String... columns) {
        for (String column : columns) {
            try {
                String value = rs.getString(column);
                if (value != null && !value.isBlank()) {
                    return value;
                }
            } catch (SQLException ignored) {
                // Try the next candidate column.
            }
        }
        return "";
    }

