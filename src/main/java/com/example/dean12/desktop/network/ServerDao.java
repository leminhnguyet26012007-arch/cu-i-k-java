    public void deleteStudent(String maSV) {
        try (Connection conn = getConnection()) {
            conn.setAutoCommit(false);
            try {
                // Get user_id first
                long userId = -1;
                try (PreparedStatement ps = conn.prepareStatement("SELECT user_id FROM sinh_vien WHERE ma_sv = ?")) {
                    ps.setString(1, maSV);
                    try (ResultSet rs = ps.executeQuery()) {
                        if (rs.next()) userId = rs.getLong(1);
                    }
                }

                // Delete related records (Transaction ensures atomic delete)
                try (PreparedStatement ps = conn.prepareStatement("DELETE FROM diem WHERE sinh_vien_ma_sv = ?")) {
                    ps.setString(1, maSV);
                    ps.executeUpdate();
                }
                try (PreparedStatement ps = conn.prepareStatement("DELETE FROM dang_ky_hoc WHERE sinh_vien_ma_sv = ?")) {
                    ps.setString(1, maSV);
                    ps.executeUpdate();
                }
                try (PreparedStatement ps = conn.prepareStatement("DELETE FROM attendance WHERE sinh_vien_ma_sv = ?")) {
                    ps.setString(1, maSV);
                    ps.executeUpdate();
                }
                try (PreparedStatement ps = conn.prepareStatement("DELETE FROM phan_hoi WHERE sinh_vien_ma_sv = ?")) {
                    ps.setString(1, maSV);
                    ps.executeUpdate();
                }
                try (PreparedStatement ps = conn.prepareStatement("DELETE FROM sinh_vien WHERE ma_sv = ?")) {
                    ps.setString(1, maSV);
                    ps.executeUpdate();
                }

                // Delete user
                if (userId != -1) {
                    try (PreparedStatement ps = conn.prepareStatement("DELETE FROM users WHERE id = ?")) {
                        ps.setLong(1, userId);
                        ps.executeUpdate();
                    }
                }

                conn.commit();
                logActivity("ADMIN", "DELETE_STUDENT", "SUCCESS", "maSV: " + maSV);
            } catch (SQLException ex) {
                conn.rollback();
                logActivity("ADMIN", "DELETE_STUDENT", "FAIL", ex.getMessage());
                throw ex;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

