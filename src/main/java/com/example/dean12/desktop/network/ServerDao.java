    public static String computeLetterGrade(double tongKet) {
        if (tongKet >= 8.5) return "A";
        if (tongKet >= 7.0) return "B";
        if (tongKet >= 5.5) return "C";
        if (tongKet >= 4.0) return "D";
        return "F";
    }

    public double[] getStudentGpaSummary(String maSV) {
        List<Diem> grades = getStudentGrades(maSV);
        if (grades.isEmpty()) {
            return new double[]{0, 0, 0};
        }
        double sum = 0;
        int pass = 0;
        for (Diem d : grades) {
            sum += d.getDiemTongKet();
            if (d.getDiemTongKet() >= 4.0) pass++;
        }
        return new double[]{sum / grades.size(), grades.size(), pass};
    }
