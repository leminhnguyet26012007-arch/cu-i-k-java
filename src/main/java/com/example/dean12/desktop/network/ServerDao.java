    // --- USER / AUTH ---
    public User login(String username, String password) {
        if (username == null || password == null) {
            return null;
        }
        username = username.trim();
        password = password.trim();

        String sql = "SELECT * FROM users WHERE username = ?";
        try (Connection conn = getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, username);
            try (ResultSet rs = ps.executeQuery()) {
                if (!rs.next()) {
                    System.out.println("[Login] Không tìm thấy user: " + username);
                    return null;
                }
                if (readLocked(rs)) {
                    System.out.println("[Login] Tài khoản bị khóa: " + username);
                    return null;
                }
                String dbPass = rs.getString("password");
                if (!com.example.dean12.desktop.PasswordUtil.matches(password, dbPass)) {
                    System.out.println("[Login] Sai mật khẩu: " + username);
                    return null;
                }
                User u = new User();
                u.setId(rs.getLong("id"));
                u.setUsername(rs.getString("username"));
                u.setPassword(dbPass);
                u.setEmail(rs.getString("email"));
                u.setRole(rs.getString("role"));
                u.setLocked(false);
                System.out.println("[Login] Thành công: " + username + " (" + u.getRole() + ")");
                return u;
            }
        } catch (SQLException e) {
            System.err.println("[Login] Lỗi SQL: " + e.getMessage());
            e.printStackTrace();
        }
        return null;
    }

    private boolean readLocked(ResultSet rs) {
        try {
            return rs.getBoolean("locked");
        } catch (SQLException e) {
            return false;
        }
    }

