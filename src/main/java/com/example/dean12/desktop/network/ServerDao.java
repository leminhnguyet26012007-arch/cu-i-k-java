    // --- CONNECTION MANAGER ---
    public static Connection getConnection() throws SQLException {
        String url = ConfigUtil.getProperty("database.url");
        try {
            if (url != null && url.contains("jdbc:h2:")) {
                Class.forName("org.h2.Driver");
            } else if (url != null && url.contains("jdbc:postgresql:")) {
                Class.forName("org.postgresql.Driver");
            } else {
                throw new SQLException("Unsupported database.url. Use jdbc:postgresql for Supabase or jdbc:h2 for local fallback.");
            }
        } catch (ClassNotFoundException e) {
            throw new RuntimeException("Không tìm thấy driver cơ sở dữ liệu", e);
        }
        return DriverManager.getConnection(
            url,
            ConfigUtil.getProperty("database.username"),
            ConfigUtil.getProperty("database.password")
        );
    }

