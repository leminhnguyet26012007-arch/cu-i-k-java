    public String registerClass(String maSV, Long lhpId) {
        SinhVien svInfo = getStudentByProperties(maSV);
        if (svInfo != null && !svInfo.isTuitionPaid()) {
            return "Vui lòng đóng học phí kỳ học hiện tại trước khi đăng ký thêm lớp!";
        }

        String sqlCheck = "SELECT id FROM dang_ky_hoc WHERE sinh_vien_ma_sv = ? AND lop_hoc_phan_id = ?";
        String sqlCap = "SELECT si_so_hien_tai, si_so_toi_da FROM lop_hoc_phan WHERE id = ?";
        String sqlReg = "INSERT INTO dang_ky_hoc (sinh_vien_ma_sv, lop_hoc_phan_id) VALUES (?, ?)";
        String sqlUpd = "UPDATE lop_hoc_phan SET si_so_hien_tai = si_so_hien_tai + 1 WHERE id = ?";

        try (Connection conn = getConnection()) {
            try (PreparedStatement ps = conn.prepareStatement(sqlCheck)) {
                ps.setString(1, maSV);
                ps.setLong(2, lhpId);
                try (ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) return "Bạn đã đăng ký lớp này rồi!";
                }
            }
            try (PreparedStatement ps = conn.prepareStatement(sqlCap)) {
                ps.setLong(1, lhpId);
                try (ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                        if (rs.getInt(1) >= rs.getInt(2)) return "Lớp đã đầy!";
                    } else return "Lớp không tồn tại!";
                }
            }
            conn.setAutoCommit(false);
            try {
                try (PreparedStatement ps = conn.prepareStatement(sqlReg)) {
                    ps.setString(1, maSV);
                    ps.setLong(2, lhpId);
                    ps.executeUpdate();
                }
                try (PreparedStatement ps = conn.prepareStatement(sqlUpd)) {
                    ps.setLong(1, lhpId);
                    ps.executeUpdate();
                }
                conn.commit();
                logActivity(maSV, "REGISTER_CLASS", "SUCCESS", "classId: " + lhpId);
                return "Đăng ký thành công!";
            } catch (SQLException ex) {
                conn.rollback();
                logActivity(maSV, "REGISTER_CLASS", "FAIL", ex.getMessage());
                throw ex;
            }
        } catch (SQLException e) {
            e.printStackTrace();
            return "Lỗi Server: " + e.getMessage();
        }
    }

