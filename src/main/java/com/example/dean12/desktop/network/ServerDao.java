    public void createStudent(SinhVien sv, String username, String password) {
        String sqlUser = "INSERT INTO users (username, password, email, role) VALUES (?, ?, ?, 'STUDENT')";
        String sqlSV = "INSERT INTO sinh_vien (ma_sv, ho_ten, lop, email, sdt, user_id) VALUES (?, ?, ?, ?, ?, ?)";

        try (Connection conn = getConnection()) {
            conn.setAutoCommit(false);
            try {
                // 1. Create User
                long userId = -1;
                try (PreparedStatement psUser = conn.prepareStatement(sqlUser, Statement.RETURN_GENERATED_KEYS)) {
                    psUser.setString(1, username);
                    psUser.setString(2, com.example.dean12.desktop.PasswordUtil.encode(password));
                    psUser.setString(3, sv.getEmail()); // plain text email for spring security web UI if needed
                    psUser.executeUpdate();
                    try (ResultSet rs = psUser.getGeneratedKeys()) {
                        if (rs.next()) userId = rs.getLong(1);
                    }
                }

                if (userId == -1) throw new SQLException("Failed to create user");

                // 2. Create SinhVien (Sensitive fields AES encrypted)
                try (PreparedStatement psSV = conn.prepareStatement(sqlSV)) {
                    psSV.setString(1, sv.getMaSV());
                    psSV.setString(2, sv.getHoTen());
                    psSV.setString(3, sv.getLop());
                    psSV.setString(4, AesUtil.encrypt(sv.getEmail()));
                    psSV.setString(5, AesUtil.encrypt(sv.getSdt()));
                    psSV.setLong(6, userId);
                    psSV.executeUpdate();
                }

                conn.commit();
                logActivity("ADMIN", "CREATE_STUDENT", "SUCCESS", "maSV: " + sv.getMaSV());
            } catch (SQLException e) {
                conn.rollback();
                logActivity("ADMIN", "CREATE_STUDENT", "FAIL", e.getMessage());
                throw e;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

